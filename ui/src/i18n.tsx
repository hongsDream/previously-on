export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>(readInitialLanguage);

  useEffect(() => {
    document.documentElement.lang = language;
  }, [language]);

  const setLanguage = useCallback((nextLanguage: Language) => {
    setLanguageState(nextLanguage);
    updatePreferences({ language: nextLanguage });
  }, []);

  const value = useMemo<I18nValue>(() => ({
    language,
    locale: language === 'ko' ? 'ko-KR' : 'en-US',
    setLanguage,
    t: (message, values) => interpolate(language === 'ko' ? KOREAN_MESSAGES[message] ?? message : message, values),
  }), [language, setLanguage]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

function readInitialLanguage(): Language {
  const stored = readPreferences();
  if (stored.language) return stored.language;
  return navigator.language.toLowerCase().startsWith('ko') ? 'ko' : 'en';
}

function interpolate(message: string, values?: Record<string, string | number>) {
  if (!values) return message;
  return message.replace(/\{(\w+)\}/g, (placeholder, key: string) => key in values ? String(values[key]) : placeholder);
}
